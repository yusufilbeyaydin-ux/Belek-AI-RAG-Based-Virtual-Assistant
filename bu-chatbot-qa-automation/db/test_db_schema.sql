                'action', 'CLEANUP',
                'deleted_count', v_count,
                'retention_days', p_retention_days
            ),
            NOW(), NOW(),
            NULL, NOW()
        );
    END IF;

    RETURN v_count;

EXCEPTION
    WHEN OTHERS THEN
        RAISE;
END;
$function$
