-- DROP FUNCTION public.fn_audit_trigger();

CREATE OR REPLACE FUNCTION public.fn_audit_trigger()
 RETURNS trigger
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
DECLARE
    v_record_id INTEGER;
    v_user_id BIGINT;
    v_op_user_id BIGINT;
    v_action VARCHAR(20);
BEGIN
    BEGIN
        v_user_id := current_setting('app.current_user_id', TRUE)::BIGINT;
    EXCEPTION
        WHEN OTHERS THEN
            v_user_id := NULL;
    END;

    IF v_user_id IS NULL OR v_user_id = 0 THEN
        SELECT us.user_id
        INTO v_user_id
        FROM public.user_schemas us
        WHERE us.schema_name = current_user
        LIMIT 1;
    END IF;

    v_op_user_id := v_user_id;

    IF TG_OP = 'DELETE' THEN
        v_action := 'DELETE';
    ELSIF TG_OP = 'UPDATE' THEN
        v_action := 'UPDATE';
    ELSE
        v_action := 'INSERT';
    END IF;

    -- record_id belirleme
    IF TG_TABLE_NAME = 'user_roles' THEN
        IF TG_OP = 'DELETE' THEN
            v_record_id := OLD.user_id;
        ELSE
            v_record_id := NEW.user_id;
        END IF;
    ELSE
        IF TG_OP = 'DELETE' THEN
            v_record_id := OLD.id;
        ELSE
            v_record_id := NEW.id;
        END IF;
    END IF;

    IF TG_OP = 'INSERT' THEN
        BEGIN
            NEW.created_by := v_user_id;
        EXCEPTION
            WHEN OTHERS THEN NULL;
        END;
    END IF;

    INSERT INTO public.audit_logs(
        user_id,
        table_name,
        record_id,
        action,
        archive_action,
        old_data,
        new_data,
        create_date,
        update_date,
        operation_user_id,
        archive_date
    )
    VALUES (
        v_user_id,
        TG_TABLE_NAME,
        v_record_id,
        v_action,
        v_action,
        CASE WHEN TG_OP = 'INSERT' THEN NULL ELSE to_jsonb(OLD) END,
        CASE WHEN TG_OP = 'DELETE' THEN NULL ELSE to_jsonb(NEW) END,
        NOW(),
        NOW(),
        v_op_user_id,
        NOW()
    );

    IF TG_OP = 'DELETE' THEN
        RETURN OLD;
    ELSE
        RETURN NEW;
    END IF;
END;
$function$
;

COMMENT ON FUNCTION public.fn_audit_trigger() IS 'AFTER INSERT/UPDATE/DELETE triggerı.
Amac: Her veri degisikligini audit_logs tablosuna kaydeder.
- app.current_user_id session degiskeninden kullanici ID alir
- Bulamazsa user_schemas tablosundan current_user ile eslestirir
- INSERT: old_data NULL, new_data dolu
- UPDATE: hem old_data hem new_data dolu
- DELETE: old_data dolu, new_data NULL
- created_by kolonunu da INSERT sirasinda set etmeye calisir';

-- DROP FUNCTION public.fn_set_audit_columns();

CREATE OR REPLACE FUNCTION public.fn_set_audit_columns()
 RETURNS trigger
 LANGUAGE plpgsql
AS $function$
DECLARE
    v_user_id BIGINT;
BEGIN
    SELECT us.user_id
    INTO v_user_id
    FROM public.user_schemas us
    WHERE us.schema_name = current_user
      AND us.is_active = TRUE
    LIMIT 1;

    IF TG_OP = 'INSERT' THEN
        IF EXISTS (
            SELECT 1 FROM information_schema.columns 
            WHERE table_name = TG_TABLE_NAME 
              AND column_name = 'created_at'
        ) THEN
            NEW.created_at := COALESCE(NEW.created_at, CURRENT_TIMESTAMP);
        END IF;

        IF EXISTS (
            SELECT 1 FROM information_schema.columns 
            WHERE table_name = TG_TABLE_NAME 
              AND column_name = 'created_by'
        ) THEN
            NEW.created_by := COALESCE(NEW.created_by, v_user_id);
        END IF;
    END IF;

    IF EXISTS (
        SELECT 1 FROM information_schema.columns 
        WHERE table_name = TG_TABLE_NAME 
          AND column_name = 'updated_at'
    ) THEN
        NEW.updated_at := CURRENT_TIMESTAMP;
    END IF;

    IF EXISTS (
        SELECT 1 FROM information_schema.columns 
        WHERE table_name = TG_TABLE_NAME 
          AND column_name = 'updated_by'
    ) THEN
        NEW.updated_by := v_user_id;
    END IF;

    RETURN NEW;
END;
$function$
;

COMMENT ON FUNCTION public.fn_set_audit_columns() IS 'BEFORE INSERT/UPDATE triggerı.
Amac: create_date, update_date, created_by, updated_by kolonlarini otomatik doldurur.
- INSERT: create_date ve created_by set edilir
- INSERT/UPDATE: update_date ve updated_by her zaman set edilir
- app.current_user_id session degiskeninden kullanici ID alinir
- Developer manuel set etmek zorunda kalmaz, atlanamaz';

