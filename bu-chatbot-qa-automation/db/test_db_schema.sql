CREATE OR REPLACE FUNCTION public.reset_user_password(p_email character varying, p_token_id integer, p_new_password_hash character varying)
 RETURNS void
 LANGUAGE plpgsql
AS $function$
DECLARE
    v_token_valid BOOLEAN;
BEGIN
    SELECT EXISTS (
        SELECT 1 FROM public.password_reset_tokens
        WHERE id = p_token_id
          AND is_used = FALSE
          AND expires_at > NOW()
    ) INTO v_token_valid;

    IF NOT v_token_valid THEN
        RAISE EXCEPTION 'TOKEN_INVALID_OR_EXPIRED: token_id=%', p_token_id;
    END IF;

    UPDATE public.users
    SET password_hash = p_new_password_hash,
        update_date   = NOW()
    WHERE email = p_email;

    IF NOT FOUND THEN
        RAISE EXCEPTION 'USER_NOT_FOUND: email=%', p_email;
    END IF;

    UPDATE public.password_reset_tokens
    SET is_used = TRUE
    WHERE id = p_token_id;

EXCEPTION
    WHEN OTHERS THEN
        RAISE;
END;
$function$
;

